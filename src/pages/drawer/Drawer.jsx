const AppBar = styled(MuiAppBar, {
  shouldForwardProp: (prop) => prop !== 'open',
})(({ theme, open }) => ({
  zIndex: theme.zIndex.drawer + 1,
  transition: theme.transitions.create(['width', 'margin'], {
    easing: theme.transitions.easing.sharp,
    duration: theme.transitions.duration.leavingScreen,
  }),
  ...(open && {
    marginLeft: drawerWidth,
    width: `calc(100% - ${drawerWidth}px)`,
    transition: theme.transitions.create(['width', 'margin'], {
      easing: theme.transitions.easing.sharp,
      duration: theme.transitions.duration.enteringScreen,
    }),
  }),
}));

const Drawer = styled(MuiDrawer, { shouldForwardProp: (prop) => prop !== 'open' })(
  ({ theme, open }) => ({
    width: drawerWidth,
    flexShrink: 0,
    whiteSpace: 'nowrap',
    boxSizing: 'border-box',
    ...(open && {
      ...openedMixin(theme),
      '& .MuiDrawer-paper': openedMixin(theme),
    }),
    ...(!open && {
      ...closedMixin(theme),
      '& .MuiDrawer-paper': closedMixin(theme),
    }),
  }),
);

export default function MiniDrawer(props) {
  const theme = useTheme();
  const [open, setOpen] = React.useState(false);

  const handleDrawer = () => {
    setOpen(!open);
  };

  let hangleIconButton = (input)=>{
    if (input === "Note") {
      props.drawerAction("AllNote")
    }

    if (input === "Archive") {
      props.drawerAction("isArchive")
    }

    if (input === "Trash") {
      props.drawerAction("isTrash")
    }
  }

  let iconTextData = [
    {
      text:"Note", icon:<LightbulbOutlinedIcon/>
    },
    {
      text:"Reminder", icon:<NotificationsOutlinedIcon/>
    },
    {
      text:"Edit labels", icon:<EditOutlinedIcon/>
    },
    {
      text:"Archive", icon:<ArchiveOutlinedIcon/>
    },
    {
      text:"Trash", icon:<DeleteOutlineOutlinedIcon/>
    }
  ];

  return (
    <Box  sx={{ display: 'flex' }} className="drawer_container">
      {/* <CssBaseline /> */}
        <PrimarySearchAppBar open={handleDrawer}/>
      <Drawer variant="permanent" className="drawer" open={open} sy>
       
        <List>
          { iconTextData.map((item, index) => (
            <ListItem key={index.text} disablePadding sx={{ display: 'block' }}>
              <ListItemButton
                sx={{
                  minHeight: 48,
                  justifyContent: open ? 'initial' : 'center',
                  px: 2.5,
                }}

                onClick={()=>hangleIconButton(item.text)}
              >
                <ListItemIcon
                  sx={{
                    minWidth: 0,
                    mr: open ? 3 : 'auto',
                    justifyContent: 'center',
                  }}
                >
                  {item.icon}
                </ListItemIcon>
                <ListItemText primary={item.text} sx={{ opacity: open ? 1 : 0 }} />
              </ListItemButton>
            </ListItem>
          ))}
        </List>
       
      </Drawer>
     
    </Box>
  );
}
